#!/usr/bin/env node
// Generates src/blogMeta.ts from blogs/*.md — slug, season, episode, title, deck
// (NO body). Runs as a `prebuild` step so the metadata index ships in the main
// bundle while article bodies stay in on-demand chunks.
import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const blogsDir = join(root, 'blogs')
const out = join(root, 'src', 'blogMeta.ts')

function seasonEpisode(slug) {
  let m = slug.match(/^ep-(\d+)/)
  if (m) return [1, Number(m[1])]
  m = slug.match(/^s(\d+)e(\d+)/)
  if (m) return [Number(m[1]), Number(m[2])]
  return [0, 0]
}

const files = readdirSync(blogsDir).filter((f) => f.endsWith('.md')).sort()

const metas = files.map((file) => {
  const raw = readFileSync(join(blogsDir, file), 'utf8')
  const slug = file.replace(/\.md$/, '')
  const [season, episode] = seasonEpisode(slug)
  const title = (raw.match(/^#\s+(.+)$/m)?.[1] ?? slug).trim()
  const deck = (raw.match(/^##\s+(.+)$/m)?.[1] ?? '').trim()
  return { slug, season, episode, title, deck }
})

const banner = '// AUTO-GENERATED by scripts/gen-blog-index.mjs — do not edit by hand.\n'
const body =
  banner +
  'export interface ArticleMeta {\n' +
  '  slug: string\n  season: number\n  episode: number\n  title: string\n  deck: string\n}\n\n' +
  `export const articleMetas: ArticleMeta[] = ${JSON.stringify(metas, null, 2)}\n`

writeFileSync(out, body)
console.log(`gen-blog-index: wrote ${metas.length} articles to src/blogMeta.ts`)
